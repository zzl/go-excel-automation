func NewIIcon(pUnk *win32.IUnknown, addRef bool, scoped bool) *IIcon {
	if pUnk == nil {
		return nil
	}
	p := (*IIcon)(unsafe.Pointer(pUnk))
	if addRef {
		pUnk.AddRef()
	}
	if scoped {
		com.AddToScope(p)
	}
	return p
}

