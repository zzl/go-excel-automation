func NewIPicture(pUnk *win32.IUnknown, addRef bool, scoped bool) *IPicture {
	 if pUnk == nil {
		return nil;
	}
	p := (*IPicture)(unsafe.Pointer(pUnk))
	if addRef {
		pUnk.AddRef()
	}
	if scoped {
		com.AddToScope(p)
	}
	return p
}

