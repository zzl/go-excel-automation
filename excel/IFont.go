func NewIFont(pUnk *win32.IUnknown, addRef bool, scoped bool) *IFont {
	p := (*IFont)(unsafe.Pointer(pUnk))
	if addRef {
		pUnk.AddRef()
	}
	if scoped {
		com.AddToScope(p)
	}
	return p
}

