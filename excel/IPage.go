// 000244A2-0001-0000-C000-000000000046
var IID_IPage = syscall.GUID{0x000244A2, 0x0001, 0x0000, 
	[8]byte{0xC0, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x46}}

type IPage struct {
	win32.IDispatch
}

func NewIPage(pUnk *win32.IUnknown, addRef bool, scoped bool) *IPage {
	p := (*IPage)(unsafe.Pointer(pUnk))
	if addRef {
		pUnk.AddRef()
	}
	if scoped {
		com.AddToScope(p)
	}
	return p
}

func (this *IPage) IID() *syscall.GUID {
	return &IID_IPage
}

func (this *IPage) GetLeftHeader(rhs **HeaderFooter) com.Error {
	addr := (*this.LpVtbl)[7]
	ret, _, _ := syscall.SyscallN(addr, uintptr(unsafe.Pointer(this)), uintptr(unsafe.Pointer(rhs)))
	if com.CurrentScope != nil {
		com.CurrentScope.Add(unsafe.Pointer(&(*rhs).IUnknown))
	}
	return com.Error(ret)
}

